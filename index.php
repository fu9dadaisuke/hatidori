                        <script>
                            window.onload = function() {
                                // 傾きをもたせる対象の指定
                                var targetImgs = '[data-ref="hero[el]"]';
                                new Glide('.glide', {
                                    // オプション
                                    focusAt: "center",
                                    startAt: 1,
                                    autoplay: 350000,
                                    animationDuration: 1e3,
                                    rewindDuration: 2e3,
                                    touchRatio: .25,
                                    perTouch: 1
                                }).mount({
                                    // 傾き制御
                                    Coverflow: function(glideWrap, setting, glide) {
                                        var tiltFunc = {
                                            // カレント制御・各処理の呼び出し
                                            tilt: function(activeList) {
                                                // カレント制御
                                                activeList.querySelector(targetImgs).style.transform = "perspective(700px) rotateY(0deg)";
                                                $('.next_1').remove();
                                                $('.next_2').remove();
                                                $('.pre_1').remove();
                                                $('.pre_2').remove();


                                                // 前・次の要素の傾き制御
                                                this.tiltPrevElements(activeList);
                                                this.tiltNextElements(activeList);
                                            },
                                            // 前の要素の制御
                                            tiltPrevElements: function(activeList) {
                                                for (var slideList = function(activeList) {
                                                        var slideList = [];
                                                        if (activeList) {
                                                            for (; activeList = activeList.previousElementSibling;) {
                                                                slideList.push(activeList);
                                                            }
                                                        }
                                                        return slideList
                                                    }(activeList), cnt = 0; cnt < slideList.length; cnt++) {
                                                    var prevImg = slideList[cnt].querySelector(targetImgs);
                                                    prevImg.style.transformOrigin = "100% 50%";
                                                    prevImg.style.transform = "perspective(700px) rotateY(30deg) skewY(10deg) scaleX(0.9)";
                                                    prevImg.insertAdjacentHTML('afterend', '<div class="pre_1"></div><div class="pre_2"></div>');
                                                    var img_width = prevImg.width;
                                                    var img_height = prevImg.height;
                                                    var pre_1_wh = document.getElementsByClassName("pre_1");
                                                    pre_1_wh[0].style.width = img_width + "px";
                                                    pre_1_wh[0].style.height = img_height + "px";
                                                    var pre_2_wh = document.getElementsByClassName("pre_2");
                                                    pre_2_wh[0].style.width = img_width + "px";
                                                    pre_2_wh[0].style.height = img_height + "px";

                                                }
                                            },
                                            // 次の要素の制御
                                            tiltNextElements: function(activeList) {
                                                for (var slideList = function(activeList) {
                                                        var slideList = [];
                                                        if (activeList) {
                                                            for (; activeList = activeList.nextElementSibling;) {
                                                                slideList.push(activeList);
                                                            }
                                                        }
                                                        return slideList;
                                                    }(activeList), cnt = 0; cnt < slideList.length; cnt++) {
                                                    var nextImg = slideList[cnt].querySelector(targetImgs);
                                                    nextImg.style.transformOrigin = "0% 50%";
                                                    nextImg.style.transform = "perspective(700px) rotateY(-30deg) skewY(-10deg) scaleX(0.9)";
                                                    nextImg.insertAdjacentHTML('afterend', '<div class="next_1"></div><div class="next_2"></div>');
                                                    var img_width = nextImg.width;
                                                    var img_height = nextImg.height;
                                                    var next_1_wh = document.getElementsByClassName("next_1");
                                                    next_1_wh[0].style.width = img_width + "px";
                                                    next_1_wh[0].style.height = img_height + "px";
                                                    var next_2_wh = document.getElementsByClassName("next_2");
                                                    next_2_wh[0].style.width = img_width + "px";
                                                    next_2_wh[0].style.height = img_height + "px";


                                                    console.log(img_width);
                                                    console.log(img_height);

                                                }
                                            }
                                        };
                                        return glide.on(["mount.after", "run"], function() {
                                            tiltFunc.tilt(setting.Html.slides[glideWrap.index])
                                        }), tiltFunc
                                    }
                                });
                            }

